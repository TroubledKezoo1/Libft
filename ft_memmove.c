#include <stdio.h>

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	unsigned char	*d = (unsigned char *)dst;
	unsigned char	*s = (unsigned char *)src;
	
	if (!dst && !src)
		return (NULL);
	if (dst < src)
		ft_memcpy(dst, src, len);
	else if (dst > src)
	{
		while (len--)
			*(d + len) = *(s + len);
	}
	return (dst);
}
